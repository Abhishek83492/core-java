class Xxxvalueofmethod
{
     public static void main(String[] args)
     {
           Integer i = new Integer(300);
            System.out.println("Byte value is------"+i.byteValue());
            System.out.println("Float value is------"+i.floatValue());
            System.out.println("Interger value is------"+i.intValue());
            System.out.println("Short value is------"+i.shortValue());
            System.out.println("Double value is------"+i.doubleValue());
            System.out.println("Long value is------"+i.longValue());
            System.out.println("Reference value is---------"+i);
      }
}