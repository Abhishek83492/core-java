class Valueofmethod
{
     public static void main(String[] args)
     {
           Integer i = Integer.valueOf("100");
           System.out.println("Integer value is---------"+i);
           Float f = Float.valueOf("20.5");
           System.out.println("Float value is-----------"+f);
           Character c = Character.valueOf('v');
           System.out.println("Character value is-------------"+c);
           Byte b = Byte.valueOf("20");
           System.out.println("Byte value is----------------"+b);
           Short s = Short.valueOf("65");
           System.out.println("Short value is----------------"+s);
           Long l = Long.valueOf("56");
           System.out.println("Long value is-----------------"+l);
           Boolean b1 = Boolean.valueOf("true");
          System.out.println("Boolean value is---------------"+b1);
     
     }
}